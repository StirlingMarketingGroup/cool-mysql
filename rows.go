type MapRow map[string]any

type MapRows []MapRow

type (
	Row  = MapRow
	Rows = MapRows
)

func Value[T any](v any) T {
	dest := new(T)
	if err := convertAssignRows(dest, v); err != nil {
		// Return zero value if conversion fails
		return *dest
	}

	return *dest
}

